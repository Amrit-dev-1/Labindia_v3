    <script>
        document.addEventListener("DOMContentLoaded", function() {
            let currentIndex = 0;
            const images = document.querySelectorAll(".team");
            const totalImages = images.length;
            let intervalId;

            function showImage(index) {
                images.forEach((img, i) => {
                    img.classList.remove("active");
                    if (i === index) {
                        img.classList.add("active");
                    }
                });
            }

            function startSlider() {
                showImage(currentIndex);
                currentIndex = (currentIndex + 1) % totalImages;
            }

            function startAutoSlide() {
                intervalId = setInterval(startSlider, 5000); // Change image every 5 seconds
            }

            function stopAutoSlide() {
                clearInterval(intervalId);
            }

            // Start the slider initially
            startAutoSlide();
            startSlider(); // Initial call to display the first image

            // Add click event listeners to all images to stop the carousel and set clicked image as active
            images.forEach((img, index) => {
                img.addEventListener("click", () => {
                    stopAutoSlide();
                    showImage(index);
                });
            });
        });
    </script>

