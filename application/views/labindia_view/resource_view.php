    <script>
        function showContent(sectionId, element) {
            // Hide all content sections
            var sections = document.getElementsByClassName('content-section');
            for (var i = 0; i < sections.length; i++) {
                sections[i].style.display = 'none';
            }
            // Show the selected content section
            document.getElementById(sectionId).style.display = 'block';

            // Remove 'active' class from all buttons
            var buttons = document.getElementsByClassName('sidebar-btn');
            for (var i = 0; i < buttons.length; i++) {
                buttons[i].classList.remove('active');
            }
            // Add 'active' class to the clicked button
            element.classList.add('active');
        }

        // Show the first section by default
        document.getElementById('use-of-site').style.display = 'block';
    </script>

    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const menuItems = document.querySelectorAll('.faq-menu .list-group-item');
            const faqItems = document.querySelectorAll('.faq-content .faq-item');

            menuItems.forEach(item => {
                item.addEventListener('click', function(e) {
                    e.preventDefault();
                    menuItems.forEach(menuItem => menuItem.classList.remove('active'));
                    this.classList.add('active');

                    faqItems.forEach(faqItem => {
                        faqItem.style.display = 'none';
                        const icon = faqItem.querySelector('.dropdown-icon');
                        icon.textContent = '▼'; // Reset icon to closed state
                    });

                    const targetId = this.getAttribute('href');
                    const activeItem = document.querySelector(targetId);
                    activeItem.style.display = 'block';
                    const icon = activeItem.querySelector('.dropdown-icon');
                    icon.textContent = '▼'; // Reset icon to closed state
                });
            });

            faqItems.forEach(faqItem => {
                faqItem.querySelector('h5').addEventListener('click', function() {
                    const body = faqItem.querySelector('.card-body');
                    const icon = faqItem.querySelector('.dropdown-icon');
                    const isVisible = body.style.display === 'block';

                    // Toggle visibility
                    body.style.display = isVisible ? 'none' : 'block';
                    icon.textContent = isVisible ? '▼' : '▲'; // Change icon based on visibility
                });
            });
        });
    </script>

